public class Variable {
     public static void main(String[] args) {
	int a = 6;
        int b = 2;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));

	double c = 10.5;
        double d = 7;
        System.out.println("c / d = " + c / d);     

	double length = 7.5;
	double width = 3.8;
	System.out.println("Area = " + length * width);     
     }
}
